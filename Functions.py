# A function is a collection of code that performs a specific task

def says_hi():
    print("Hello Ahmed")

says_hi()

def says_hi_2(name):
    print("Hello " + name)

says_hi_2("Ahmed")

def says_hi_3(name, age):
    print("Hello " + name)
    print("You are " + age )

says_hi_3("Ahmed", "26")